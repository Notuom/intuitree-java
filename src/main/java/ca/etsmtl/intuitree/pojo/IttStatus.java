package ca.etsmtl.intuitree.pojo;

public class IttStatus {

    private String name;
    private String color;

    public IttStatus(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }
}
